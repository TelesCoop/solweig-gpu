def _saturated_vapor_pressure(ta):
    ta_k = np.asarray(ta, dtype=float) + 273.15
    return np.exp(
        -5.8002206e3 / ta_k
        + 1.3914993
        - 4.8640239e-2 * ta_k
        + 4.1764768e-5 * ta_k**2
        - 1.4452093e-8 * ta_k**3
        + 6.5459673 * np.log(ta_k)
    )


def specific_humidity(ta, rh):
    psv = _saturated_vapor_pressure(ta)
    pv = psv * np.asarray(rh, dtype=float) / 100
    w = 0.621945 * pv / (101325 - pv)
    return w / (1 + w)


def wind_speed_from_svf(u_met, svf):
    svf = np.asarray(svf, dtype=float)
    wvr = WVR_B * svf**2 - (2.0 / 3.0) * WVR_B * svf**3
    return np.maximum(u_met * wvr, WIND_FLOOR)


def pet_polynomial(d_tmrt, ta, va, rh):
    huss = specific_humidity(ta, rh)
    bucket = int(np.argmin(np.abs(_HUSS_VALUES - huss)))

    ta = np.asarray(ta, dtype=float)
    dtra = np.asarray(d_tmrt, dtype=float)
    va = np.asarray(va, dtype=float)

    pet = np.full(np.broadcast(ta, dtra, va).shape, _INTERCEPTS[bucket], dtype=float)
    for i, j, k, coef in _COEFFS[bucket]:
        pet = pet + coef * ta**i * dtra**j * va**k
    return pet
